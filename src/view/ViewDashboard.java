    private void jpGProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpGProductosMouseClicked
        this.jlBanner.setText("Gestión de Productos");
        
        if(gestionProductos==false)
        {
            this.jpGProductos.setBackground(jpBanner.getBackground());
            this.iconProductos.setIcon(new ImageIcon(getClass().getResource("/images/flechaAbajo.png")));
            
            jpProductosElaborados.setVisible(true);
            jpGInsumos.setVisible(true);
            
            gestionProductos=true;
        }
        else
        {
            this.jpGProductos.setBackground(jpEncabezado.getBackground());
            this.iconProductos.setIcon(new ImageIcon());
            
            jpProductosElaborados.setVisible(false);
            jpGInsumos.setVisible(false);
            
            gestionProductos=false;
        }
    }//GEN-LAST:event_jpGProductosMouseClicked

